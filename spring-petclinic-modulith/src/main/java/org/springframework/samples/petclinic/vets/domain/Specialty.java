package org.springframework.samples.petclinic.vets.domain;

import java.util.Objects;

/**
 * Pure Java value object representing a veterinarian specialty.
 *
 * This is a framework-agnostic POJO containing only business logic.
 * No JPA, Spring, or validation annotations.
 *
 * @author PetClinic Team
 */
public class Specialty {

    private Integer id;
    private String name;

    public Specialty() {
    }

    public Specialty(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getters and Setters

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Specialty specialty = (Specialty) o;
        return Objects.equals(id, specialty.id);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id);
    }

    @Override
    public String toString() {
        return "Specialty{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
